function drawPixels(pixelColors) {
    for (let i = 0; i < arrY; i++) {
      for (let j = 0; j < arrX; j++) {
        switch (pixelColors[i][j]) {
          case 0:
            fill(colorWhite); //white
            break;
          case 1:
            fill(colorBlack); //black
            break;
        }
        noStroke();
        rect(XPos + j * 20, YPos + i * 20, 20, 20);
        console.log("making pixels");
      }
    }
}

